// FAST Modal
let modalBtnFast = document.querySelector('.modal-btn-fast');
let modalBgFast = document.querySelector('.modal-bg-fast');
let modalCloseFast = document.querySelector('.modal-close-fast');
let descriptionFast = document.querySelector('.description-box-fast');

modalBtnFast.addEventListener('mouseenter', () => {
    descriptionFast.classList.add('description-box-mouseover-fast');
});

modalBtnFast.addEventListener('mouseleave', () => {
    descriptionFast.classList.remove('description-box-mouseover-fast');
});

modalBtnFast.addEventListener('click', () => {
    modalBgFast.classList.add('bg-active-modal-fast');
});

modalCloseFast.addEventListener('click', () => {
    modalBgFast.classList.remove('bg-active-modal-fast')
});

